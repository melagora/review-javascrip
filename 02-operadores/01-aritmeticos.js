let a=5;
let b=7;

console.log (a+b, "Adición"); //Adición
console.log (a-b, "Resta"); //Resta
console.log (a*b, "Multiplicación"); //Multiplicación
console.log (a/b, "División"); //División
console.log (a%b, "Módulo"); //Módulo (lo que sobra de la división)
console.log (a**b, "Potencia"); //Potencia
console.log("");

//Incrementar
console.log("Incrementar:")
console.log("Valor inicial de la variable: ",a);
console.log(++a); //Le suma 1 y luego muestra el valor
console.log(a++); //Muestra el valor y luego le suma 1
console.log(a); //Muestra el valor ya modificado
console.log("");

a=5;
//Decrementar
console.log("Decrementar:")
console.log("Valor inicial de la variable: ",a);
console.log(--a); //Le resta 1 y luego muestra el valor
console.log(a--); //Muestra el valor y luego le resta 1
console.log(a); //Muestra el valor ya modificado